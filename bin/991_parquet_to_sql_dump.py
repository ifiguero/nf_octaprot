#!/usr/bin/env -S uv run --with polars python3.12

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Any
import os
import sys

import polars as pl


SILVER_ROOT = Path(os.environ["SILVER_DIR"])


@dataclass(frozen=True)
class Column:
    name: str
    dtype: str
    nullable: bool = True


TABLE_TO_PARQUET = {
    "repositories": SILVER_ROOT / "repositories",
    "files": SILVER_ROOT / "files",
    "replicates": SILVER_ROOT / "replicates",
}


REPOSITORIES_SCHEMA = {
    "table": "repositories",
    "description": "Repository inventory",
    "columns": [
        Column("id", "string", False),
        Column("url", "string", False),
        Column("doi", "string", False),
    ],
}


FILES_SCHEMA = {
    "table": "files",
    "description": "Discovered repository files",
    "columns": [
        Column("id", "string"),
        Column("repository_id", "string", False),
        Column("remote_path", "string", False),
        Column("size_bytes", "int64"),
        Column("scan_timestamp", "timestamp"),
    ],
}


REPLICATES_SCHEMA = {
    "table": "replicates",
    "description": "Input dataset",
    "columns": [
        Column("id", "string", False),
        Column("organism", "string", False),
        Column("source_type", "string", False),
        Column("material", "string", False),
        Column("condition", "string", False),
        Column("sample_group", "string", False),
    ],
}


ALL_SCHEMAS = {
    "repositories": REPOSITORIES_SCHEMA,
    "files": FILES_SCHEMA,
    "replicates": REPLICATES_SCHEMA,
}


def sql_quote(value: Any) -> str:
    if value is None:
        return "NULL"

    if isinstance(value, bool):
        return "TRUE" if value else "FALSE"

    if isinstance(value, (int, float)):
        return str(value)

    text = str(value).replace("'", "''")

    return f"'{text}'"


def create_table_statement(schema: dict) -> str:
    type_map = {
        "string": "TEXT",
        "bool": "BOOLEAN",
        "int32": "INTEGER",
        "int64": "BIGINT",
        "float32": "REAL",
        "float64": "DOUBLE PRECISION",
        "timestamp": "TIMESTAMP",
    }

    columns = []

    for col in schema["columns"]:
        sql_type = type_map.get(col.dtype, "TEXT")
        nullable = "" if col.nullable else " NOT NULL"
        columns.append(f"    {col.name} {sql_type}{nullable}")

    body = ",\n".join(columns)

    return (
        f"CREATE TABLE IF NOT EXISTS {schema['table']} (\n"
        f"{body}\n"
        ");\n"
    )


def load_table_dataframe(table_name: str) -> pl.DataFrame:
    parquet_root = TABLE_TO_PARQUET.get(table_name)

    if parquet_root is None:
        raise ValueError(
            f"No parquet location configured for table '{table_name}'"
        )

    if not parquet_root.exists():
        return pl.DataFrame()

    parquet_files = sorted(parquet_root.rglob("*.parquet"))

    if not parquet_files:
        return pl.DataFrame()

    return pl.concat(
        [pl.read_parquet(path) for path in parquet_files],
        how="diagonal_relaxed",
    )


def apply_filters(
    dataframe: pl.DataFrame,
    filters: dict[str, Any] | None = None,
) -> pl.DataFrame:
    if dataframe.is_empty():
        return dataframe

    if not filters:
        return dataframe

    result = dataframe

    for column, value in filters.items():
        if column not in result.columns:
            continue

        if isinstance(value, list):
            result = result.filter(pl.col(column).is_in(value))
        else:
            result = result.filter(pl.col(column) == value)

    return result


def insert_statements(
    table_name: str,
    dataframe: pl.DataFrame,
) -> list[str]:
    if dataframe.is_empty():
        return []

    statements: list[str] = []

    columns = dataframe.columns
    column_sql = ", ".join(columns)

    for row in dataframe.iter_rows(named=True):
        values_sql = ", ".join(
            sql_quote(row[column])
            for column in columns
        )

        statements.append(
            f"INSERT INTO {table_name} "
            f"({column_sql}) "
            f"VALUES ({values_sql});"
        )

    return statements


def build_dump(
    filters: dict[str, Any] | None = None,
) -> str:
    sql_parts: list[str] = []

    sql_parts.append("-- generated by parquet_to_sql_dump.py")
    sql_parts.append("BEGIN;")
    sql_parts.append("")

    for table_name, schema in ALL_SCHEMAS.items():
        sql_parts.append(f"-- table: {table_name}")
        sql_parts.append(create_table_statement(schema))

        df = load_table_dataframe(table_name)
        df = apply_filters(df, filters)

        sql_parts.extend(
            insert_statements(
                table_name,
                df,
            )
        )

        sql_parts.append("")

    sql_parts.append("COMMIT;")

    return "\n".join(sql_parts)


def main() -> int:
    if len(sys.argv) != 2:
        print(
            f"usage: {sys.argv[0]} <output.sql>",
            file=sys.stderr,
        )
        return 1

    output_path = Path(sys.argv[1]).with_suffix('.sql')

    sql = build_dump()

    output_path.write_text(sql, encoding="utf-8")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
